for i in range(1,11):    #use this to run 10 times
    print(int((i+1)*i/2))  #caculate by formula
# This formula works because the sum of the first n natural numbers can be visualized as a triangle.
# For example, the first triangular number is 1 (1 point), the second is 3 (1+2 points), the 3rd is 6 (1+2+3 points), and so on.